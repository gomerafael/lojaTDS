    private void jTableEntradaDeProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableEntradaDeProdutosMouseClicked
        int linha = jTableEntradaDeProdutos.getSelectedRow();
        Produto ev = produtoModel.getProduto(linha);
        jTextFieldEstoqueAtual.setText("" + ev.getCodigo());
        jTextFieldProdutoEstoqueAtual.setText(ev.getNome());
        jTextFieldTotalEstoqueAtual.setText(String.valueOf(ev.getQuantidade()));
    }//GEN-LAST:event_jTableEntradaDeProdutosMouseClicked

